results

param_grid = {
    "n_estimators": [50, 100],
    "max_depth": [10, None],
}

grid_search = GridSearchCV(
    RandomForestRegressor(random_state=42),
    param_grid,
    cv=2,
    scoring="neg_mean_absolute_error",
    n_jobs=-1
)

